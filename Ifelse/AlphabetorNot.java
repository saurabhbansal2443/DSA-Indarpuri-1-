public class AlphabetorNot {
    public static void main(String[] args) {
        // Write a program to check whether a given character is an Alphabet or not.
        char ch = 'a';
        if (ch >= 'a' && ch <= 'z') {
            System.out.println("aplhabet");
        } else if (ch >= 'A' && ch <= 'z') {
            System.out.println("aplhabet");
        } else {
            System.out.println("Not a aplhabet");
        }
    }
}
